#include <stdio.h> //biblioteca de comunicação do usuário
#include <stdlib.h> //biblioteca de alocação de espaço em memória
#include <locale.h> //biblioteca de alocação de texto por região
#include <string.h> //bibliotca responsável por cuidar das string

int registro() //função responsável por registrar os usuários no sistema
{
	//inicio da criação de variáveis/strings do registro
	char arquivo[40];
	char cpf[40];
	char nome[40];
	char sobrenome[40];
	char cargo[40];
	//final da criação de variáveis/strings do registro
	
	printf("Digite o CPF a ser cadastrado: "); //coletando informação do usuário
	scanf("%s", cpf);
	
	strcpy(arquivo, cpf); //Responsável por copiar os valores das strings
	
	FILE *file; //cria o arquivo
	file = fopen(arquivo, "w"); //cria o arquivo
	fprintf(file,cpf); //salva o valor da variavel
	fclose(file); // fecha o arquivo
	
	file = fopen(arquivo, "a"); 
	fprintf(file, ",");
	fclose(file);
	
	printf("Digite o nome a ser cadastrado: "); //salva o nome a ser registrado
	scanf("%s",nome);
	
	file = fopen(arquivo, "a");
	fprintf(file,nome);
	fclose(file);
	
	file = fopen(arquivo, "a");
	fprintf(file, ",");
	fclose(file);
	
	printf("Digite o sobrenome a ser cadastrado: "); //salva o sobrenome a ser registrado
	scanf("%s",sobrenome);
	
	file = fopen(arquivo, "a");
	fprintf(file,sobrenome);
	fclose(file);
	
	file = fopen(arquivo, "a");
	fprintf(file, ",");
	fclose(file);
	
	printf("Digite o cargo a ser cadastrado: "); //salva o cargo a ser registrado
	scanf("%s",cargo);
	
	file = fopen(arquivo, "a");
	fprintf(file,cargo);
	fclose(file);
	
	system("pause");
	//fim do sistema de registro
}

int consulta() //função responsável por consultar um CPF já registrado
{
	setlocale(LC_ALL, "Portuguese");
	
	char cpf[40];
	char conteudo[200];
	
	printf("Digite o CPF a ser consultado: "); //recebe o cpf a ser consultado
	scanf("%s",cpf);
	
	FILE *file; //abrir 
	file = fopen(cpf,"r");
	
	if(file == NULL)
	{
		printf("Não foi possivel abrir o arquivo, não localizado!.\n");
	}
	
	while(fgets(conteudo, 200, file) != NULL)
	{
		printf("\nEstas são as informações do usuário: ");
		printf("%s", conteudo);
		printf("\n\n");	
	}
	fclose(file);
	system("pause");
	//fim do sistema de consulta
}


int deletar() //função responsavel pela exclusão do CPF já registrado
{
	char cpf[40];
	
	printf("Digite o CPF a ser deletado: "); //coletando o CPF a ser deletado
	scanf("%s",cpf);
	
	FILE *file;
	file = fopen(cpf,"r");
	
	if(file == NULL) //função para o caso de o usuário não estar registrado
	{
		fclose(file);
		printf("O usuário não se encontra registrado no sistema!.\n");
		system("pause");
	}
	
	else if (file != NULL)
	{
		fclose(file);
		printf("Usuário deletado com sucesso!\n");
		system("pause");
		remove (cpf);
	}
	
}



